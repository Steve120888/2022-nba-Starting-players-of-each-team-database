CREATE TABLE TEAM (			
TEAM_ID 		INTEGER PRIMARY KEY,
TEAM_NAME			VARCHAR(35),
TEAM_FOUNDYEAR 	VARCHAR(35) NOT NULL,
TEAM_CITY 			VARCHAR(35),
TEAM_MANAGER		VARCHAR(35),
STADIUM_LOCATION 		VARCHAR(35),
STADIUM_NAME 			VARCHAR(35),
);

CREATE TABLE PLAYER (
PLAYER_ID 			INTEGER PRIMARY KEY,
PLAYER_NAME		VARCHAR(35),
PLAYER_POSITION		VARCHAR(35) NOT NULL,
PLAYER_HEIGHT		INTEGER NOT NULL,
PLAYER_WEIGHT		INTEGER NOT NULL,
TEAM_ID 			INTEGER,
PLAYER_AVGPOINT 		INTEGER,
PLAYER_AVGREBOUND 	INTEGER,
PLAYER_AVGASSIST	INTEGER);

ALTER TABLE		 PLAYER
ADD FOREIGN KEY  (TEAM_ID) REFERENCES TEAM;


CREATE TABLE COACHES(
COACH_ID 		INTEGER PRIMARY KEY,
COACH_NAME 			VARCHAR(35),
TEAM_ID 				INTEGER,
FOREIGN KEY (TEAM_ID) REFERENCES TEAM);
/*_____________________________________________________*/

CREATE TABLE GAME(
GAME_ID			INTEGER PRIMARY KEY,
GAME_WINNER 		VARCHAR(35),
HOME_TEAM_POINT		INTEGER,
AWAY_TEAM_POINT		INTEGER,
HOME_TEAMID  		INTEGER,
AWAY_TEAMID   		INTEGER,
GAME_DATE			DATETIME,
FOREIGN KEY (HOME_TEAMID) REFERENCES TEAM(TEAM_ID),
FOREIGN KEY (AWAY_TEAMID) REFERENCES TEAM(TEAM_ID));

